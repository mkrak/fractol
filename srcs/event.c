#include "../includes/fdf.h"


void		ft_zoom(t_coef *e, int x, int y)
{
		e->imx += x / 5;
		e->imy += y / 5;
		e->zoom *= 1.2;
		e->imx *= 1.2;
		e->imy *= 1.2;
}

void		ft_dezoom(t_coef *e, int x, int y)
{
		e->imx -= x / 5;
		e->imy -= y / 5;
		e->zoom /= 1.2;
		e->imx /= 1.2;
		e->imy /= 1.2;
}
